<?php

namespace App\Entity;

use App\Repository\TypePersonneRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=TypePersonneRepository::class)
 */
class TypePersonne
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $typePersonne;

    /**
     * @ORM\OneToMany(targetEntity=Personne::class, mappedBy="typePersonne")
     */
    private $personnes;

    public function __construct()
    {
        $this->personnes = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTypePersonne(): ?string
    {
        return $this->typePersonne;
    }

    public function setTypePersonne(string $typePersonne): self
    {
        $this->typePersonne = $typePersonne;

        return $this;
    }

    /**
     * @return Collection<int, Personne>
     */
    public function getPersonnes(): Collection
    {
        return $this->personnes;
    }

    public function addPersonne(Personne $personne): self
    {
        if (!$this->personnes->contains($personne)) {
            $this->personnes[] = $personne;
            $personne->setTypePersonne($this);
        }

        return $this;
    }

    public function removePersonne(Personne $personne): self
    {
        if ($this->personnes->removeElement($personne)) {
            // set the owning side to null (unless already changed)
            if ($personne->getTypePersonne() === $this) {
                $personne->setTypePersonne(null);
            }
        }

        return $this;
    }
}
